const BADGE_ICONS = [
  IconSparkles,
  IconCheckCircle,
  IconMail,
  IconChatBubble,
  IconBookOpen,
  IconClipboard,
  IconDroplet,
  IconWalk,
] as const

function hashBadgeId(id: string): number {
  let h = 0
  for (let i = 0; i < id.length; i++) h = (h * 31 + id.charCodeAt(i)) >>> 0
  return h
}

export function BadgeIconById({ badgeId, className = 'w-8 h-8 text-amber-600' }: { badgeId: string; className?: string }) {
  const Icon = BADGE_ICONS[hashBadgeId(badgeId) % BADGE_ICONS.length]
  return <Icon className={className} />
}

export function DailyChallengeIcon({ iconKey, className = 'w-5 h-5 text-diapal-600 shrink-0' }: { iconKey: ChallengeIconKey; className?: string }) {
  const map: Record<ChallengeIconKey, ReactNode> = {
    droplet: <IconDroplet className={className} />,
    walk: <IconWalk className={className} />,
    leaf: <IconLeaf className={className} />,
    clipboard: <IconClipboard className={className} />,
    moon: <IconMoon className={className} />,
    apple: <IconApple className={className} />,
  }
  return <>{map[iconKey]}</>
}
